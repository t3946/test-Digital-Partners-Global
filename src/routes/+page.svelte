<script lang="ts">
	/**
	 * Dependence
	 */
	import PostsApi from '$lib/api/methods/posts';
	import { getReasonPhrase } from 'http-status-codes';

	/**
	 * Components
	 */
	import Feed from '$lib/components/feed/Feed.svelte';
	import Loader from '$lib/components/shared/Loader.svelte';
	import ErrorComponent from '$lib/components/shared/Error.svelte';

	/**
	 * Load data
	 * @description Метод для получения постов
	 */
	const loadData = async () => {
		const response = await PostsApi.getAll();

		if (!response.success) {
			throw new Error(getReasonPhrase(response.data.code || response.data.message));
		}

		localStorage.setItem('posts', JSON.stringify(response.data));

		return response.data;
	};

	const oldPosts = JSON.parse(localStorage.getItem('posts'));
</script>

{#await loadData()}
	<Loader />
{:then posts}
	<Feed {posts} />
{:catch e}
	<ErrorComponent>
		{e}
	</ErrorComponent>

	{#if oldPosts}
		<div>
			<h3 style="margin: 0 0 24px; color: red">Данные загружены из browser local storage</h3>

			<Feed posts={oldPosts} />
		</div>
	{:else}
		<p>Нет данных для отображения.</p>
	{/if}
{/await}
